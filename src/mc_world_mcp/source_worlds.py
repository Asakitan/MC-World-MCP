def _safe_generation_interfaces(config: ServerConfig) -> dict[str, Any]:
    try:
        return list_generation_interfaces(config)
    except Exception as exc:
        return {"error": str(exc)}


def _generation_signal(palette_counts: Counter[str], appearance: dict[str, Any]) -> dict[str, Any]:
    notable_total = sum(
        count
        for block, count in palette_counts.items()
        if _base_block_id(block) not in BASELINE_WORLD_BLOCKS
    )
    floor = appearance.get("ocean_floor_y") or {}
    floor_span = int(floor.get("span", 0)) if isinstance(floor, dict) else 0
    if notable_total and floor_span:
        strength = "strong"
        reason = "sample contains non-baseline worldgen blocks and varied floor height"
    elif notable_total:
        strength = "medium"
        reason = "sample contains non-baseline worldgen blocks"
    elif floor_span:
        strength = "medium"
        reason = "sample contains varied terrain height"
    else:
        strength = "chunks_only"
        reason = "chunks exist, but sampled blocks look like baseline terrain"
    return {
        "strength": strength,
        "reason": reason,
        "notable_block_volume": notable_total,
        "floor_height_span": floor_span,
    }


def _simulation_diagnosis(
    success: bool,
    complete: bool,
    signal: dict[str, Any],
    missing_chunks: list[dict[str, int]],
) -> str:
    if not success:
        return "No generated chunks were found in the requested source-world area."
    if not complete:
        return f"Partial server-generation sample found; {len(missing_chunks)} requested chunks are still missing."
    if signal["strength"] == "chunks_only":
        return "Requested chunks exist, but the sampled appearance is close to baseline terrain; use a larger area or inspect structure placement if a feature should appear here."
    return "Requested chunks exist and the sampled appearance contains worldgen signals."


def _simulation_next_steps(success: bool, complete: bool, source_name: str) -> list[str]:
    if not success:
        return [
            f"Run the server with level-name={source_name} and generate the requested chunk area.",
            "Stop the server before using write/import tools.",
        ]
    if not complete:
        return [
            "Generate or pregenerate the missing chunk coordinates in the source world.",
            "Run simulate_worldgen_generation again to refresh the sample.",
        ]
    return [
        "Open the preview PNGs to inspect the generated shape.",
        "Use compare_world_chunks before importing the source chunks into the target world.",
    ]


def _floor_projection_from_chunk(chunk: Any, columns: list[int]) -> list[tuple[int, int | None, str]]:
    unresolved = list(dict.fromkeys(columns))
    blocks = ["minecraft:air"] * 256
    heights: list[int | None] = [None] * 256
    sections = []
    for section in chunk.get("sections", []):
        block_states = section.get("block_states")
        if not block_states:
            continue
        palette = block_states.get("palette", [])
        if not palette:
            continue
        palette_blocks = [palette_index_to_block(section, index) for index in range(len(palette))]
        sections.append((
            int(section.get("Y", 0)),
            block_states,
            palette_blocks,
            [_base_block_id(block) for block in palette_blocks],
        ))
    sections.sort(key=lambda item: item[0], reverse=True)
    for section_y, block_states, palette_blocks, palette_base_names in sections:
        if not unresolved:
            break
        if section_y * 16 > SIMULATION_MAX_Y or section_y * 16 + 15 < SIMULATION_MIN_Y:
            continue
        indices = _decode_indices_fast(block_states)
        unresolved = _fill_floor_projection(
            indices,
            palette_blocks,
            palette_base_names,
            AIR_OR_WATER,
            unresolved,
            blocks,
            heights,
            section_y,
            SIMULATION_MIN_Y,
            SIMULATION_MAX_Y,
        )
    return [(column, heights[column], blocks[column]) for column in columns]


def _decode_indices_fast(block_states: Any) -> list[int]:
    if _PREVIEW_ACCEL is not None and hasattr(_PREVIEW_ACCEL, "decode_indices"):
        return _PREVIEW_ACCEL.decode_indices(block_states.get("data"), len(block_states["palette"]))
    return decode_indices(block_states)


def _fill_floor_projection(
    indices: list[int],
    palette: list[str],
    palette_base_names: list[str],
    skip: set[str],
    unresolved: list[int],
    blocks: list[str],
    heights: list[int | None],
    section_y: int,
    min_y: int,
    max_y: int,
) -> list[int]:
    if _PREVIEW_ACCEL is not None and hasattr(_PREVIEW_ACCEL, "fill_floor_projection"):
        return _PREVIEW_ACCEL.fill_floor_projection(indices, palette, palette_base_names, skip, unresolved, blocks, heights, section_y, min_y, max_y)

    remaining = unresolved
    palette_len = len(palette)
    for ly in range(15, -1, -1):
        next_remaining: list[int] = []
        y = section_y * 16 + ly
        if y > max_y:
            continue
        if y < min_y:
            break
        y_offset = ly << 8
        for column in remaining:
            palette_index = indices[y_offset | column]
            if 0 <= palette_index < palette_len:
                block = palette[palette_index]
                base_name = palette_base_names[palette_index]
            else:
                block = "minecraft:air"
                base_name = "minecraft:air"
            if base_name in skip:
                next_remaining.append(column)
            else:
                blocks[column] = block
                heights[column] = y
        remaining = next_remaining
        if not remaining:
            break
    return remaining


