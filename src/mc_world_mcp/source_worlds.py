def simulate_worldgen_generation(
    config: ServerConfig,
    source_world_name: str = "",
    min_cx: int = -1,
    min_cz: int = -1,
    max_cx: int = 1,
    max_cz: int = 1,
    dimension: str = "overworld",
    sample: int = 1,
    include_previews: bool = True,
    confirm: bool = False,
) -> dict[str, Any]:
    """Inspect a server-generated source world as the simulation result for worldgen.

    The MCP still does not run Minecraft's generator. This tool lets assistants
    use a local source world that was generated by the real server as a compact
    "simulation sample": chunk presence, palette signals, seafloor shape, and
    preview images are returned in one report.
    """
    source_name = source_world_name or f"{config.world_name}_regen_source"
    source_config = _world_config(config, source_name)
    min_cx, max_cx = sorted((int(min_cx), int(max_cx)))
    min_cz, max_cz = sorted((int(min_cz), int(max_cz)))
    sample = _normalize_sample(sample)
    requested_chunks = (max_cx - min_cx + 1) * (max_cz - min_cz + 1)
    if requested_chunks > MAX_SIMULATION_CHUNKS and not confirm:
        raise ValueError(f"large worldgen simulation requires confirm=true above {MAX_SIMULATION_CHUNKS} chunks")

    base = {
        "simulation_type": "server_generated_source_world",
        "server_mechanism": {
            "uses_real_minecraft_worldgen": True,
            "can_execute_worldgen_inside_mcp": False,
            "description": (
                "Run Minecraft/Arclight with the source world selected, let it generate chunks, "
                "then stop the server. This MCP inspects those generated Anvil files offline."
            ),
        },
        "accelerator": _accelerator_summary(),
        "active_world": config.world_name,
        "source_world": source_name,
        "source_path": str(source_config.world),
        "dimension": dimension,
        "requested": {
            "min_cx": min_cx,
            "min_cz": min_cz,
            "max_cx": max_cx,
            "max_cz": max_cz,
            "chunks": requested_chunks,
        },
    }
    if not source_config.world.exists():
        return {
            **base,
            "ok": False,
            "success": False,
            "complete_requested_area": False,
            "generated_chunks": 0,
            "missing_chunks": [{"cx": cx, "cz": cz} for cx in range(min_cx, max_cx + 1) for cz in range(min_cz, max_cz + 1)][:200],
            "diagnosis": "Source world does not exist yet; run the server once with this level-name or create it through your normal workflow.",
            "next_steps": [
                f"Set server.properties level-name={source_name}, or otherwise create that source world.",
                "Start Minecraft/Arclight with the same datapacks, mods, and plugins, generate the requested chunks, then stop the server.",
                "Run simulate_worldgen_generation again against the generated source world.",
            ],
        }

    presence = _chunk_presence(source_config, min_cx, min_cz, max_cx, max_cz, dimension)
    present_chunks = presence["present_chunks"]
    missing_chunks = presence["missing_chunks"]
    palette_report = _palette_report(source_config, present_chunks, dimension)
    appearance = _appearance_summary(source_config, present_chunks, min_cx, min_cz, max_cx, max_cz, dimension, sample)
    previews = _simulation_previews(source_config, min_cx, min_cz, max_cx, max_cz, dimension, sample) if include_previews else {}
    resource_report = _safe_worldgen_report(source_config)
    generation_interfaces = _safe_generation_interfaces(source_config)
    signal = _generation_signal(palette_report["palette_counts"], appearance)
    success = bool(present_chunks)
    complete = len(present_chunks) == requested_chunks

    return {
        **base,
        "ok": success,
        "success": success,
        "complete_requested_area": complete,
        "generated_chunks": len(present_chunks),
        "missing_chunks": missing_chunks[:200],
        "truncated_missing_chunks": len(missing_chunks) > 200,
        "support": detect_world_info(source_config).as_dict(),
        "coverage": scan_world_coverage(source_config, dimension),
        "generation_interfaces": generation_interfaces,
        "worldgen_resources": resource_report,
        "appearance": {
            **appearance,
            "dominant_blocks": _top_counts(palette_report["palette_counts"], 20),
            "notable_blocks": _top_counts(palette_report["notable_blocks"], 20),
            "palette_chunks_sampled": palette_report["chunks_sampled"],
            "chunk_samples": palette_report["chunk_samples"],
        },
        "generation_signal": signal,
        "previews": previews,
        "diagnosis": _simulation_diagnosis(success, complete, signal, missing_chunks),
        "next_steps": _simulation_next_steps(success, complete, source_name),
    }


def compare_world_chunks(
    config: ServerConfig,
    source_world_name: str,
    min_cx: int,
    min_cz: int,
    max_cx: int,
    max_cz: int,
    dimension: str = "overworld",
) -> dict[str, Any]:
    source_config = _world_config(config, source_world_name)
    _assert_source_not_target(config, source_config)
    min_cx, max_cx = sorted((min_cx, max_cx))
    min_cz, max_cz = sorted((min_cz, max_cz))
    source_regions: dict[Path, RegionFile] = {}
    target_regions: dict[Path, RegionFile] = {}
    source_present: list[dict[str, int]] = []
    target_present: list[dict[str, int]] = []
    missing_source: list[dict[str, int]] = []
    for cx in range(min_cx, max_cx + 1):
        for cz in range(min_cz, max_cz + 1):
            src_path, src_index = _chunk_region_path(source_config, dimension, "region", cx, cz)
            dst_path, dst_index = _chunk_region_path(config, dimension, "region", cx, cz)
            src_region = source_regions.setdefault(src_path, RegionFile(src_path))
            dst_region = target_regions.setdefault(dst_path, RegionFile(dst_path))
            chunk = {"cx": cx, "cz": cz}
            if src_region.get_raw(src_index) is None:
                missing_source.append(chunk)
            else:
                source_present.append(chunk)
            if dst_region.get_raw(dst_index) is not None:
                target_present.append(chunk)
    total = (max_cx - min_cx + 1) * (max_cz - min_cz + 1)
    return {
        "source_world": source_world_name,
        "target_world": config.world_name,
        "dimension": dimension,
        "requested": {"min_cx": min_cx, "min_cz": min_cz, "max_cx": max_cx, "max_cz": max_cz, "chunks": total},
        "source_present": len(source_present),
        "target_present": len(target_present),
        "missing_source": missing_source[:200],
        "would_import": source_present[:200],
        "truncated_lists": len(missing_source) > 200 or len(source_present) > 200,
    }


def import_chunks_from_world(
    config: ServerConfig,
    source_world_name: str,
    chunks: list[dict[str, int]],
    dimension: str = "overworld",
    include_entities: bool = True,
    include_poi: bool = True,
    confirm: bool = False,
) -> dict[str, Any]:
    if not confirm:
        raise ValueError("chunk import requires confirm=true")
    if not chunks:
        return {"ok": True, "imported_region_chunks": 0, "imported_entity_chunks": 0, "imported_poi_chunks": 0}
    source_config = _world_config(config, source_world_name)
    _assert_source_not_target(config, source_config)
    _assert_import_supported(source_config)
    roots = ["region"]
    if include_entities:
        roots.append("entities")
    if include_poi:
        roots.append("poi")

    target_files: set[Path] = set()
    imports: list[tuple[str, Path, Path, int, bytes]] = []
    missing: list[dict[str, Any]] = []
    for item in chunks:
        cx, cz = int(item["cx"]), int(item["cz"])
        for root_name in roots:
            src_path, index = _chunk_region_path(source_config, dimension, root_name, cx, cz)
            dst_path, _ = _chunk_region_path(config, dimension, root_name, cx, cz)
            raw = RegionFile(src_path).get_raw(index)
            if raw is None:
                if root_name == "region":
                    missing.append({"cx": cx, "cz": cz, "type": root_name, "source": str(src_path)})
                continue
            target_files.add(dst_path)
            imports.append((root_name, src_path, dst_path, index, raw))

    if missing:
        raise FileNotFoundError(f"source world is missing required terrain chunks: {missing[:20]}")

    backup = begin_write(config, f"import_chunks_from_world {source_world_name} {dimension}", sorted(target_files))
    target_regions: dict[Path, RegionFile] = {}
    counts = {"region": 0, "entities": 0, "poi": 0}
    source_files: set[str] = set()
    for root_name, src_path, dst_path, index, raw in imports:
        target_region = target_regions.setdefault(dst_path, RegionFile(dst_path))
        target_region.set_raw(index, raw)
        counts[root_name] += 1
        source_files.add(src_path.relative_to(config.root).as_posix())
    for region in target_regions.values():
        region.write()
    backup.write_manifest()
    return {
        "ok": True,
        "source_world": source_world_name,
        "target_world": config.world_name,
        "dimension": dimension,
        "requested_chunks": len(chunks),
        "imported_region_chunks": counts["region"],
        "imported_entity_chunks": counts["entities"],
        "imported_poi_chunks": counts["poi"],
        "source_files": sorted(source_files),
        "target_files": sorted(path.relative_to(config.root).as_posix() for path in target_files),
        "backup": str(backup.root),
    }


def _chunk_presence(
    config: ServerConfig,
    min_cx: int,
    min_cz: int,
    max_cx: int,
    max_cz: int,
    dimension: str,
) -> dict[str, list[dict[str, int]]]:
    regions: dict[Path, RegionFile] = {}
    present: list[dict[str, int]] = []
    missing: list[dict[str, int]] = []
    for cx in range(min_cx, max_cx + 1):
        for cz in range(min_cz, max_cz + 1):
            path, index = _chunk_region_path(config, dimension, "region", cx, cz)
            region = regions.setdefault(path, RegionFile(path))
            chunk = {"cx": cx, "cz": cz}
            if region.get_raw(index) is None:
                missing.append(chunk)
            else:
                present.append(chunk)
    return {"present_chunks": present, "missing_chunks": missing}


def _palette_report(config: ServerConfig, chunks: list[dict[str, int]], dimension: str) -> dict[str, Any]:
    palette_counts: Counter[str] = Counter()
    notable_blocks: Counter[str] = Counter()
    samples: list[dict[str, Any]] = []
    sampled_chunks = chunks[:MAX_PALETTE_SAMPLE_CHUNKS]
    for ordinal, item in enumerate(sampled_chunks):
        cx, cz = int(item["cx"]), int(item["cz"])
        try:
            palette = summarize_chunk_palette(config, cx, cz, dimension)
            chunk_counts = Counter({str(block): int(count) for block, count in palette["palette_counts"].items()})
            palette_counts.update(chunk_counts)
            notable = Counter({
                block: count
                for block, count in chunk_counts.items()
                if _base_block_id(block) not in BASELINE_WORLD_BLOCKS
            })
            notable_blocks.update(notable)
            if ordinal < MAX_CHUNK_DETAIL_SAMPLES:
                samples.append({
                    **inspect_chunk(config, cx, cz, dimension),
                    "dominant_blocks": _top_counts(chunk_counts, 10),
                    "notable_blocks": _top_counts(notable, 10),
                })
        except Exception as exc:
            if ordinal < MAX_CHUNK_DETAIL_SAMPLES:
                samples.append({"cx": cx, "cz": cz, "error": str(exc)})
    return {
        "palette_counts": palette_counts,
        "notable_blocks": notable_blocks,
        "chunks_sampled": len(sampled_chunks),
        "chunk_samples": samples,
    }


def _appearance_summary(
    config: ServerConfig,
    chunks: list[dict[str, int]],
    min_cx: int,
    min_cz: int,
    max_cx: int,
    max_cz: int,
    dimension: str,
    sample: int,
) -> dict[str, Any]:
    if not chunks:
        return {
            "columns_sampled": 0,
            "ocean_floor_y": None,
            "shape": "no_generated_chunks",
        }
    present = {(int(item["cx"]), int(item["cz"])) for item in chunks}
    regions: dict[Path, RegionFile] = {}
    loaded: dict[tuple[int, int], tuple[Path, RegionFile, int, Any]] = {}
    columns_by_chunk: dict[tuple[int, int], list[int]] = {}
    heights: Counter[int] = Counter()
    floor_blocks: Counter[str] = Counter()
    missing_columns = 0
    x1, x2 = min_cx * 16, max_cx * 16 + 15
    z1, z2 = min_cz * 16, max_cz * 16 + 15
    for z in range(z1, z2 + 1, sample):
        for x in range(x1, x2 + 1, sample):
            cx, cz = x >> 4, z >> 4
            if (cx, cz) not in present:
                missing_columns += 1
                continue
            columns_by_chunk.setdefault((cx, cz), []).append(((z & 15) << 4) | (x & 15))

    for (cx, cz), columns in columns_by_chunk.items():
        try:
            loaded[(cx, cz)] = loaded.get((cx, cz)) or load_chunk_with_cache(config, cx, cz, dimension, regions)
        except FileNotFoundError:
            missing_columns += len(columns)
            continue
        chunk = loaded[(cx, cz)][3]
        for column, height, block in _floor_projection_from_chunk(chunk, columns):
            if height is None:
                missing_columns += 1
            else:
                heights[height] += 1
                floor_blocks[block] += 1
    if not heights:
        return {
            "columns_sampled": missing_columns,
            "missing_columns": missing_columns,
            "ocean_floor_y": None,
            "shape": "no_solid_floor_found",
        }
    min_y, max_y = min(heights), max(heights)
    span = max_y - min_y
    if span >= 4:
        shape = "varied_terrain_or_structures"
    elif span > 0:
        shape = "slightly_varied_floor"
    else:
        shape = "flat_floor_in_sample"
    return {
        "columns_sampled": sum(heights.values()) + missing_columns,
        "missing_columns": missing_columns,
        "ocean_floor_y": {
            "min": min_y,
            "max": max_y,
            "span": span,
            "common": [{"y": y, "count": count} for y, count in heights.most_common(12)],
        },
        "floor_blocks": _top_counts(floor_blocks, 20),
        "shape": shape,
    }


def _simulation_previews(
    config: ServerConfig,
    min_cx: int,
    min_cz: int,
    max_cx: int,
    max_cz: int,
    dimension: str,
    sample: int,
) -> dict[str, Any]:
    x1, x2 = min_cx * 16, max_cx * 16 + 15
    z1, z2 = min_cz * 16, max_cz * 16 + 15
    center_x = (x1 + x2) // 2
    previews: dict[str, Any] = {}
    preview_jobs = [
        ("surface", lambda: render_map_preview(config, x1, z1, x2, z2, "surface", dimension, sample)),
        ("ocean_floor", lambda: render_map_preview(config, x1, z1, x2, z2, "ocean_floor", dimension, sample)),
        ("slice_x", lambda: render_slice_preview(config, "x", center_x, z1, z2, SIMULATION_MIN_Y, SIMULATION_MAX_Y, dimension)),
    ]
    for name, render in preview_jobs:
        try:
            previews[name] = render()
        except Exception as exc:
            previews[name] = {"ok": False, "error": str(exc)}
    return previews


